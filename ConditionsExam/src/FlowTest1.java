public class FlowTest1 {
	
	public static void main(String[] args) {
		
		
		/*
		60점이상이면 합격입니다
		60점 미만이면 불합격입니다
		*/
		int score = 0;
		
		score = 60;
		
		System.out.println("점수 :" +score);
		
		
		if(score>=60) {
			System.out.println("합격입니다");
		}
		
		if(score<60) {
			System.out.println("불합격입니다");
		}
		
		
		
	}
}
