
public class OperatorEx6 {

	public static void main(String[] args) {
		
//		단항 연산자 ++,--
		int n =5;
		n++;//n=n+1
		System.out.println(n);
		
		n=5;
		++n;//n=n+1
		System.out.println(n);
		
		n =5;
		n--;//n=n-1
		System.out.println(n);
		
		n=5;
		--n;//n=n-1
		System.out.println(n);
	}

}
