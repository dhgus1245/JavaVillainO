
public class OperatorEx8 {

	public static void main(String[] args) {
		

//		대입연산자
		
		int n = 0;
		
		n+=3;
		System.out.println(n);
		//n=n+3
		n*=3;
		System.out.println(n);
		//n=n*3
		n=10;
		System.out.println(n);
		
		n%=3;
		System.out.println(n);
		//n=n%3
		
	}

}
