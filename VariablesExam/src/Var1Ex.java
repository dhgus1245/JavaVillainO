		int birth= 1994;
		int year = currentYear-birth+1;
		
		System.out.println("저의 나이는 "+ year+ "입니다");
		//syso+(ctrl+space) = 자동완성
		System.out.println(1+year+"과연");
		System.out.println("1"+year+"과연");
		System.out.println("저의 나이는"+year+1+"입니다");
//		+ 더하기 연산자
//		concatenation 연결
